fun main(Args: Array<String>) {
    var str:String = "Hi, i'm string"
    println(str)

    var char:Char = 'A'
    println(char)

    var bool:Boolean = true
    println(bool)

    var bool2:Boolean = 4<5
    println(bool2)

    var myNum:Byte = 114
    println(myNum)

    val s:Short = 32400
    println(s)

    var integer:Int = 5845
    println(integer)

    var long:Long = 27676387264872364
    println(long)

    var float:Float = 3.45f
    println(float)

    var double:Double = 3.234234
    println(double)

    var toD = float.toDouble()
    println(toD)

    var toS: Short = integer.toShort()
    println(toS)

//    toInt(), toDouble(), toShort()
}